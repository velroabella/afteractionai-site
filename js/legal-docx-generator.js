  /* ---------- MAIN GENERATION FUNCTION ---------- */

  /**
   * Generates a .docx file and triggers download.
   * @param {string} formType — one of LEGAL_FORM_TYPES
   * @param {object} [userData] — optional user data to fill placeholders (future phase)
   */
  async function generateLegalDocx(formType, userData) {
    const D = getDocx();

    const normalized = formType.toLowerCase().replace(/[\s_]+/g, '-');
    const builder = BUILDERS[normalized];
    if (!builder) {
      throw new Error('[LegalDocx] Unknown form type: ' + formType);
    }

    // Build form-specific content
    const formContent = builder(D);

    // Assemble document
    const doc = new D.Document({
      styles: buildStyles(),
      sections: [{
        properties: {
          page: {
            size:   { width: 12240, height: 15840 },         // US Letter
            margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 }  // 1" all sides
          }
        },
        headers: { default: buildHeader(D) },
        footers: { default: buildFooter(D) },
        children: [
          // Notice section at top of document body
          ...buildNoticeSection(D),
          spacer(D),
          // Form content
          ...formContent
        ]
      }]
    });

    // Generate blob and download
    const buffer = await D.Packer.toBlob(doc);
    const fileName = normalized + '_' + new Date().toISOString().slice(0, 10) + '.docx';

    // Trigger download
    const url = URL.createObjectURL(buffer);
    const a = document.createElement('a');
    a.href = url;
    a.download = fileName;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    setTimeout(function () { URL.revokeObjectURL(url); }, 5000);

    return fileName;
  }

