  /* ---------- HELPER PARAGRAPH BUILDERS ---------- */

  function heading(D, level, text) {
    const lvl = [D.HeadingLevel.HEADING_1, D.HeadingLevel.HEADING_2, D.HeadingLevel.HEADING_3][level - 1];
    return new D.Paragraph({ heading: lvl, children: [new D.TextRun(text)] });
  }

  function para(D, text) {
    return new D.Paragraph({
      spacing: { after: 60 },
      children: [new D.TextRun({ text: text, size: 22, font: 'Arial' })]
    });
  }

  function boldPara(D, text) {
    return new D.Paragraph({
      spacing: { after: 60 },
      children: [new D.TextRun({ text: text, size: 22, font: 'Arial', bold: true })]
    });
  }

  function spacer(D) {
    return new D.Paragraph({ spacing: { after: 120 }, children: [] });
  }

